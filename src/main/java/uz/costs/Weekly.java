package uz.costs;

public class Weekly {
    private int produktta;
    private int choyxona;
    public Weekly (int produktta, int choyxona) {
        this.produktta=produktta;
        this.choyxona=choyxona;
    }
    public void setProduktta (int produktta) {
        this.produktta=produktta;
    }
    public void setChoyxona (int choyxona) {
        this.choyxona=choyxona;
    }
}
